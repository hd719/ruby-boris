# P is similar to puts but its more technical and shows the object type
# P is used for debugging

# Ex.
puts "Steven Seagal"
p "Steven Seagal"

puts 5 # This will show the integer 5
p "5" # This will show the string "5" and not the integer 5

puts "Hi there"
p "hi \n there" # This will show the string "hi \n there" and not "hi" and "there" on separate lines, get the original piece of code
