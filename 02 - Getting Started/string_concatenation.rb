# String Concatenation
# Concatenation is the process of appending one string to the end of another string. In Ruby, we can concatenate strings using the + operator.

first_name = "Harry"
last_name = "Potter"

# Concatenate first_name and last_name
full_name = first_name + " " + last_name
puts full_name

